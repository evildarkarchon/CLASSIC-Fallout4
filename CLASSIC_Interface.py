class AudioPlayer(QObject):
    """
    The AudioPlayer class manages the playback of audio notifications, providing sound
    effects for error, notification, and custom events. It enables or disables audio
    based on settings and connects signals to play corresponding sounds.

    This class uses QSoundEffect to handle pre-defined and custom audio files. The
    playback of sounds can be toggled on or off using the toggle_audio method.

    Attributes:
        play_error_signal (Signal): Signal emitted to trigger the playback of the
            error sound effect.
        play_notify_signal (Signal): Signal emitted to trigger the playback of the
            notification sound effect.
        play_custom_signal (Signal): Signal emitted to play a custom sound file
            with a specified path.
    """
    # Define signals for different sounds
    play_error_signal = Signal()
    play_notify_signal = Signal()
    play_custom_signal = Signal(str)  # Signal to play a custom sound with a path

    def __init__(self) -> None:
        """
        Initializes the audio notification settings and prepares sound effects for
        error, notification, and custom events. If the audio notification setting
        is not already configured, it sets a default value. This class initializes
        QSoundEffect objects for pre-defined audio files and connects signals to
        their respective slots for audio playback depending on the current setting.

        Raises:
            KeyError: Raises if specific keys are not found in the configuration.
        """
        super().__init__()
        self.audio_enabled = classic_settings(bool, "Audio Notifications")
        if self.audio_enabled is None:
            yaml_settings(bool, YAML.Settings, "CLASSIC_Settings.Audio Notifications", True)
            self.audio_enabled = True

        # Setup QSoundEffect objects for the preset sounds
        self.error_sound = QSoundEffect()
        self.error_sound.setSource(QUrl.fromLocalFile("CLASSIC Data/sounds/classic_error.wav"))
        self.error_sound.setVolume(0.5)  # Set max volume

        self.notify_sound = QSoundEffect()
        self.notify_sound.setSource(QUrl.fromLocalFile("CLASSIC Data/sounds/classic_notify.wav"))
        self.notify_sound.setVolume(0.5)  # Set max volume

        # Connect signals to respective slots
        if self.audio_enabled:
            self.play_error_signal.connect(self.play_error_sound)
            self.play_notify_signal.connect(self.play_notify_sound)
            self.play_custom_signal.connect(lambda file: self.play_custom_sound(file))  # Use custom path

    def play_error_sound(self) -> None:
        """
        Plays an error sound if audio is enabled and the error sound is successfully loaded.

        This method checks if audio playback is enabled and if the error sound file is
        properly loaded. If both conditions are met, it initiates playback of the error
        sound to notify the user of an error scenario.

        Returns:
            None

        """
        if self.audio_enabled and self.error_sound.isLoaded():
            self.error_sound.play()

    def play_notify_sound(self) -> None:
        """
        Plays a notification sound if audio is enabled and the sound is loaded.

        This method checks whether audio is enabled and if the notification sound
        is properly loaded in the system. If both conditions are met, it
        plays the notification sound.

        Raises:
            No specific exceptions are raised by this method.
        """
        if self.audio_enabled and self.notify_sound.isLoaded():
            self.notify_sound.play()

    @staticmethod
    def play_custom_sound(sound_path: str, volume: float = 1.0) -> None:
        """
        Plays a custom sound effect from the specified file path at a given volume.

        Args:
            sound_path (str): The file path of the sound file to play.
            volume (float, optional): The volume level to play the sound, ranging
                from 0.0 (muted) to 1.0 (maximum volume). Defaults to 1.0.

        """
        custom_sound = QSoundEffect()
        custom_sound.setSource(QUrl.fromLocalFile(sound_path))
        custom_sound.setVolume(volume)
        custom_sound.play()

    def toggle_audio(self, state: bool) -> None:
        """
        Toggles the audio functionality of the system, enabling or disabling sound signals.

        When audio is enabled, signals for notifying, error, and custom sound playback
        are connected to their respective handlers. When audio is disabled, these signals
        are disconnected to ensure no sound is played.

        Args:
            state (bool): A boolean indicating whether to enable or disable audio.
                If True, audio signals are connected, otherwise they are disconnected.
        """
        self.audio_enabled = state
        if not state:
            self.play_notify_signal.disconnect()
            self.play_error_signal.disconnect()
            self.play_custom_signal.disconnect()
        else:
            self.play_notify_signal.connect(self.play_notify_sound)
            self.play_error_signal.connect(self.play_error_sound)
            self.play_custom_signal.connect(lambda file: self.play_custom_sound(file))


